// The For Loop is used to executed an iteration a certain amount of times specified. You have a setup value for the loop variable, then a condition that has to be true, and if that is true then the final step will execute.
// Example
var ourArray = [];

for (var i = 0; i < 5; i++) {
  ourArray.push(i);
}

