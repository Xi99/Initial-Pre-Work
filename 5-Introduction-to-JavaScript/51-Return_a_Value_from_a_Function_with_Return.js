// The return statement can send a value back out of a function. You call the function outside of it with an argument for it to perform the function task.
// Example
function minusSeven(num) {
  return num - 7;
}

// Only change code below this line
function timesFive(num) {
  return num * 5;
}

console.log(timesFive(5));
console.log(minusSeven(10));
